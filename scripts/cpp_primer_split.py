def sanitize(title):
    """清理标题用于文件名"""
    name = title.replace('\x00', '')
    # C++ 特殊字符
    name = name.replace('++', '_plusplus_')
    name = name.replace('->', '_arrow_')
    name = name.replace('::', '_scope_')
    name = name.replace('#', '_sharp_')
    name = name.replace('^', '_p_')
    name = name.replace('*', '_star_')
    name = name.replace('<', '_lt_').replace('>', '_gt_')
    name = name.replace('+', '_plus_')
    name = name.replace('=', '_eq_')
    name = name.replace('&', '_and_')
    name = name.replace('|', '_or_')
    # 通用特殊字符
    name = name.replace(':', '_').replace('?', '').replace('/', '_')
    name = name.replace('\\', '_').replace('"', '').replace('"', '').replace('"', '')
    name = name.replace('—', '-').replace('–', '-')
    name = name.replace('（', '(').replace('）', ')')
    name = name.replace('【', '[').replace('】', ']')
    name = name.replace('，', '_')
    name = name.replace('：', '_')
    name = name.replace('；', '_')
    name = name.replace('（', '').replace('）', '')
    name = name.replace('(', '').replace(')', '')
    name = name.replace('[', '').replace(']', '')
    name = name.replace('{', '').replace('}', '')
    name = name.replace("'", '').replace('"', '')
    name = name.replace(',', '_')
    name = name.replace(' ', '_')
    # 清理
    name = name.strip().strip('_')
    name = re.sub(r'_+', '_', name)
    return name


def chapter_dir_name(ch_num):
    """生成章节目录名"""
    title = CHAPTER_TITLES.get(ch_num, f"Chapter_{ch_num}")
    return f"{ch_num:02d}_{sanitize(title)}"


def build_chunks():
    """构建分片列表"""
    chunks = []

    # 辅助材料（无重叠）
    for name, start, end in AUXILIARY:
        chunks.append({
            "title": name,
            "start": start,
            "end": end,
            "dir": "00_辅助材料",
            "filename": f"{name}.pdf",
            "category": "辅助材料",
        })

    # 内容分片
    for i, (sec_id, title, page, ch_num, sec_type) in enumerate(SECTIONS):
        if i + 1 < len(SECTIONS):
            next_page = SECTIONS[i + 1][2]
            next_ch = SECTIONS[i + 1][3]
            if next_ch == ch_num:
                end_page = next_page  # 同章：1页重叠
            else:
                end_page = next_page - 1  # 跨章：精确切割
        else:
            # 最后一个分片：延伸到附录A结束
            end_page = 864  # 包含索引页到最后一页

        if end_page < page:
            continue  # 跳过0页条目

        ch_dir = chapter_dir_name(ch_num)
        safe_title = sanitize(title)
        file_name = f"{sec_id}_{safe_title}.pdf"

        chunks.append({
            "title": f"{sec_id} {title}",
            "start": page,
            "end": end_page,
            "dir": ch_dir,
            "filename": file_name,
            "category": sec_type,
        })

    return chunks


def execute_split(chunks):
    """执行分片"""
    reader = PdfReader(INPUT_PDF)
    file_count = 0
    total_split_pages = 0
    csv_rows = []

    for chunk in chunks:
        out_dir = os.path.join(OUTPUT_ROOT, chunk["dir"])
        os.makedirs(out_dir, exist_ok=True)
        out_path = os.path.join(out_dir, chunk["filename"])

        writer = PdfWriter()
        page_count = 0
        for p in range(chunk["start"], chunk["end"] + 1):
            writer.add_page(reader.pages[p - 1])
            page_count += 1

        with open(out_path, "wb") as f:
            writer.write(f)

        file_count += 1
        total_split_pages += page_count
        csv_rows.append([
            chunk["dir"],
            chunk["filename"],
            chunk["title"],
            chunk["start"],
            chunk["end"],
            page_count,
            chunk["category"],
        ])
        print(f"  [{file_count:3d}] {chunk['dir']}/{chunk['filename']} "
              f"(p{chunk['start']}-p{chunk['end']}, {page_count}页)")

    # 写CSV
    csv_path = os.path.join(OUTPUT_ROOT, "分片清单.csv")
    with open(csv_path, "w", newline="", encoding="utf-8-sig") as f:
        writer = csv.writer(f)
        writer.writerow(["目录", "文件名", "标题", "起始页", "结束页", "页数", "类型"])
        writer.writerows(csv_rows)

    return file_count, total_split_pages


def main():
    print(f"[1] PDF信息: {TOTAL_PAGES}页, {len(SECTIONS)}个小节")

    chunks = build_chunks()
    print(f"[2] 分片数: {len(chunks)}")

    os.makedirs(OUTPUT_ROOT, exist_ok=True)
    file_count, total_split_pages = execute_split(chunks)

    # 验证
    print(f"\n[验证] 原始: {TOTAL_PAGES}页")
    print(f"[验证] 分片: {file_count}个文件, {total_split_pages}页 (含重叠)")

    # 页面覆盖检查
    all_covered = set()
    for chunk in chunks:
        all_covered.update(range(chunk["start"], chunk["end"] + 1))
    all_pages = set(range(1, TOTAL_PAGES + 1))
    missing = sorted(all_pages - all_covered)
    if missing:
        print(f"[警告] 缺失页面: {missing}")
    else:
        print(f"[验证] 页面覆盖: 完整 ✓")

    overlap = total_split_pages - TOTAL_PAGES
    print(f"[验证] 重叠页数: {overlap}")


if __name__ == "__main__":
    main()
